/** 전체 전기사업법 핵심 조문 */
export const ESA_ARTICLES: EsaArticle[] = [
  ...INSPECTION_ARTICLES,
  ...SAFETY_MGMT_ARTICLES,
  ...BUSINESS_ARTICLES,
];

/** 조문 ID로 조회 */
export function getESAArticle(id: string): EsaArticle | null {
  return ESA_ARTICLES.find(a => a.id === id) ?? null;
}

/** 키워드 검색 */
export function searchESA(query: string): EsaArticle[] {
  const q = query.toLowerCase();
  return ESA_ARTICLES.filter(a =>
    a.keywords.some(k => k.includes(q)) ||
    a.title.toLowerCase().includes(q) ||
    a.summary.toLowerCase().includes(q),
  );
}

