export const NER_ARTICLES: NerArticle[] = [
  ...BRANCH_CIRCUIT_ARTICLES,
  ...PROTECTION_ARTICLES,
  ...WIRING_ARTICLES,
];

/** 조문 ID로 조회 */
export function getNERArticle(id: string): NerArticle | null {
  return NER_ARTICLES.find(a => a.id === id) ?? null;
}

/** 키워드 검색 (한국어) */
export function searchNER(query: string): NerArticle[] {
  const q = query.toLowerCase();
  return NER_ARTICLES.filter(a =>
    a.keywords.some(k => k.includes(q)) ||
    a.title.toLowerCase().includes(q) ||
    a.summary.toLowerCase().includes(q),
  );
}

