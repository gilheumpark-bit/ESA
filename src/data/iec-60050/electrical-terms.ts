// =============================================================================
// PART 1: Type Definition
// IEC 60050 기반 전기공학 용어 사전
// =============================================================================

export interface ElectricalTerm {
  /** 고유 식별자 (카테고리-순번) */
  id: string;
  /** 한국어 용어 */
  ko: string;
  /** 영어 용어 */
  en: string;
  /** 일본어 용어 (선택) */
  ja?: string;
  /** 중국어 용어 (선택) */
  zh?: string;
  /** 동의어/약어 목록 */
  synonyms: string[];
  /** 분류 카테고리 */
  category: ElectricalTermCategory;
  /** IEC 60050 참조 번호 */
  iecRef?: string;
  /** 관련 계산기 ID */
  relatedCalc?: string;
}

export type ElectricalTermCategory =
  | 'power-system'
  | 'protection'
  | 'cable-wire'
  | 'grounding'
  | 'renewable'
  | 'motor'
  | 'measurement'
  | 'standard';

