export const ELECTRICAL_TERMS: ElectricalTerm[] = [
  ...POWER_SYSTEM_TERMS,
  ...PROTECTION_TERMS,
  ...CABLE_WIRE_TERMS,
  ...GROUNDING_TERMS,
  ...RENEWABLE_TERMS,
  ...MOTOR_TERMS,
  ...MEASUREMENT_TERMS,
  ...STANDARD_TERMS,
];

/** 카테고리별 용어 조회 헬퍼 */
export function getTermsByCategory(category: ElectricalTermCategory): ElectricalTerm[] {
  return ELECTRICAL_TERMS.filter((t) => t.category === category);
}

/** ID로 용어 조회 */
export function getTermById(id: string): ElectricalTerm | undefined {
  return ELECTRICAL_TERMS.find((t) => t.id === id);
}

/** 검색어로 용어 검색 (한/영/약어) */
export function searchTerms(query: string): ElectricalTerm[] {
  const q = query.toLowerCase();
  return ELECTRICAL_TERMS.filter(
    (t) =>
      t.ko.toLowerCase().includes(q) ||
      t.en.toLowerCase().includes(q) ||
      t.synonyms.some((s) => s.toLowerCase().includes(q)) ||
      (t.ja && t.ja.toLowerCase().includes(q)) ||
      (t.zh && t.zh.toLowerCase().includes(q))
  );
}
